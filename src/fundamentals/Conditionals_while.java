package fundamentals;

public class Conditionals_while {
    public static void main(String[] args) {
        /*repetitive tasks*/
        int counter = 0;

        //while (condition), execute this
        while(counter <= 10){
            System.out.println(counter);
            counter++;
        }

        /*0
        * 1
        * 2
        * ... 10*/


    }
}
